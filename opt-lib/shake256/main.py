import hashlib


def test_ax1000000():
    h = hashlib.shake_256()
    for _ in range(1000000):
        h.update(b'a')
    r = h.digest(32)
    assert bytearray(r) == bytearray([
        0x35, 0x78, 0xa7, 0xa4, 0xca, 0x91, 0x37, 0x56, 0x9c, 0xdf, 0x76, 0xed, 0x61, 0x7d, 0x31, 0xbb,
        0x99, 0x4f, 0xca, 0x9c, 0x1b, 0xbf, 0x8b, 0x18, 0x40, 0x13, 0xde, 0x82, 0x34, 0xdf, 0xd1, 0x3a,
    ])


def test_chain():
    m = bytearray(32)
    for _ in range(25000):
        h = hashlib.shake_256()
        h.update(m)
        m = bytearray(h.digest(32))
    assert bytearray(m) == bytearray([
        0x41, 0xa6, 0x43, 0xad, 0xe0, 0x13, 0xc0, 0xe9, 0x9b, 0x31, 0x48, 0x42, 0x40, 0xc3, 0xd1, 0x15,
        0x94, 0x5d, 0x0e, 0xae, 0xed, 0xda, 0xc9, 0x2f, 0xfe, 0xf0, 0x47, 0xf2, 0xea, 0x87, 0x32, 0x3f,
    ])


def test_empty():
    h = hashlib.shake_256()
    r = h.digest(32)
    assert bytearray(r) == bytearray([
        0x46, 0xb9, 0xdd, 0x2b, 0x0b, 0xa8, 0x8d, 0x13, 0x23, 0x3b, 0x3f, 0xeb, 0x74, 0x3e, 0xeb, 0x24,
        0x3f, 0xcd, 0x52, 0xea, 0x62, 0xb8, 0x1b, 0x82, 0xb5, 0x0c, 0x27, 0x64, 0x6e, 0xd5, 0x76, 0x2f,
    ])


def test_hello_world():
    h = hashlib.shake_256()
    h.update(b'Hello, World!')
    r = h.digest(32)
    assert r == bytearray([
        0xb3, 0xbe, 0x97, 0xbf, 0xd9, 0x78, 0x83, 0x3a, 0x65, 0x58, 0x8c, 0xea, 0xe8, 0xa3, 0x4c, 0xf5,
        0x9e, 0x95, 0x58, 0x5a, 0xf6, 0x20, 0x63, 0xe6, 0xb8, 0x9d, 0x07, 0x89, 0xf3, 0x72, 0x42, 0x4e,
    ])


if __name__ == '__main__':
    test_ax1000000()
    test_chain()
    test_empty()
    test_hello_world()
